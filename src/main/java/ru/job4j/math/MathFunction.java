package ru.job4j.math;

public class MathFunction {
    public static double sum(double fist, double second) {
        return fist + second;
    }

    public static double multiplay(double fist, double second) {
        return fist * second;
    }

    public static double difference(double fist, double second) {
        return fist - second;
    }

    public static double division(double fist, double second) {
        return fist / second;
    }
}
